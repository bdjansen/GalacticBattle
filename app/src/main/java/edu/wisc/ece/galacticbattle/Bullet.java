public class Bullet {
    private int x;
    private int y;

    public Bullet(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int setY() {
        return y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
